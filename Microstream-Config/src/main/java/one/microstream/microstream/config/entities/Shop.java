
package one.microstream.microstream.config.entities;

/**
 * Shop
 */
public class Shop
{

	private long id;
	
	private String name;
	
	public Shop()
	{
	}
	
	public long getId()
	{
		return this.id;
	}

	public void setId(final long id)
	{
		this.id = id;
	}
	
	public String getName()
	{
		return this.name;
	}

	public void setName(final String name)
	{
		this.name = name;
	}
	
}
